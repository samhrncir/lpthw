print("Is it true that 3 + 2 < 5 - 7?")

# Will output the solution to the result of the comparison condition.
print(3 + 2 < 5 - 7)

# Will output string followed by the math solution.
print("What is 3 + 2?", 3 + 2)
print("What is 5 - 7?", 5 - 7)

